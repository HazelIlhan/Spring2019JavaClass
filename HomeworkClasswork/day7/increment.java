package day7;

public class increment {
	public static void main(String[] args) {
int apples=0;
System.out.println(apples);
	System.out.println(++apples);
	System.out.println(apples);
	System.out.println(apples++);
	System.out.println(apples);
	apples--;
	System.out.println(apples);
	
	
	}
}